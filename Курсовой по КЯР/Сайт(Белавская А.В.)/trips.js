function al1() {
            al1 = document.all.album1.style
            back = document.all.back.style
            al1.opacity = 1;
            al1.zIndex = 100;
            back.zIndex = 99;
            back.opacity = 0.7;
            document.all.back.onclick = function fun() {
                al1.opacity = 0;
                al1.zIndex = -100;
                back.zIndex = -100;
                back.opacity = 0;
                window.location.reload();
            };
        }
